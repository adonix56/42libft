#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	if ((!s2 && s1) || (s2 && !s1))
		return (0);
	if ((!s1 && !s2) || !ft_strcmp(s1, s2))
		return (1);
	return (0);
}
